"""
- python 字面量的写法
"""

# 单行注释

print("Hello word")

# 定义一个整型1的变量
a = 1

# 定义一个浮点型1.1的变量
b = 1.1

# 定义一个字符串的变量
c = "hello"

# 定义一个布尔型的变量
d = True

# 定义一个空值的变量
e = None

# 定义一个列表的变量
f = [1, 2, 3, 4, 5]

# 定义一个元组的变量
g = (1, 2, 3, 4, 5)

# 定义一个字典的变量
h = {"name": "张三", "age": 18}

# 定义一个集合的变量
i = {1, 2, 3, 4, 5}

# 打印变量的值
print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print(g)
print(h)
print(i)

# 打印变量的类型
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))
print(type(g))
print(type(h))
print(type(i))

# 打印变量的内存地址
print(id(a))
print(id(b))
print(id(c))
print(id(d))
print(id(e))
print(id(f))
print(id(g))
print(id(h))
print(id(i))


